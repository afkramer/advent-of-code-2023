package adventofcode2023.day10;

public class Coordinate {
    private int xVal;
    private int yVal;

    public Coordinate(int xVal, int yVal) {
        this.xVal = xVal;
        this.yVal = yVal;
    }

    public int getxVal() {
        return xVal;
    }

    public int getyVal() {
        return yVal;
    }

}
